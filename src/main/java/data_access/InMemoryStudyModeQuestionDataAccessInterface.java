package data_access;

import use_case.studymodequestion.StudyModeQuestionDataAccessInterface;

/**
 * In-memory implementation of the DAO for topic chosen in Study Mode Question. This implementation does
 * NOT persist data between runs of the program.
 */
public class InMemoryStudyModeQuestionDataAccessInterface implements StudyModeQuestionDataAccessInterface {

    private String option;

    @Override
    public void saveSelectedOption(String option) {
        this.option = option;
    }

    @Override
    public String getSelectedOption() {
        return this.option;
    }

}
